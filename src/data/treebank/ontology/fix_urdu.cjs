// Process a file to replace placeholder Urdu translations
function processFile(filename) {
  const filepath = path.join(DIR, filename);
  let content = fs.readFileSync(filepath, 'utf-8');
  let changeCount = 0;

  // For each translation entry, find and replace in the file
  for (const [english, urdu] of Object.entries(TRANSLATIONS)) {
    // Escape special regex characters in the English text
    const escaped = english.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');

    // Pattern: ur: "ENGLISH_TEXT" where the ur value matches the English text
    // This handles the case where the script set ur = en as placeholder
    const pattern = new RegExp(`(ur: ")${escaped}(")`, 'g');
    const newContent = content.replace(pattern, `$1${urdu}$2`);

    if (newContent !== content) {
      const matches = content.match(pattern);
      if (matches) changeCount += matches.length;
      content = newContent;
    }
  }

  fs.writeFileSync(filepath, content, 'utf-8');
  return changeCount;
}

