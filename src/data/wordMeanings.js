// Available languages in the dataset
export const AVAILABLE_LANGUAGES = {
  en: { name: 'English', nativeName: 'English', direction: 'ltr' },
  ur: { name: 'Urdu', nativeName: 'اردو', direction: 'rtl' },
  fr: { name: 'French', nativeName: 'Français', direction: 'ltr' },
  de: { name: 'German', nativeName: 'Deutsch', direction: 'ltr' },
  id: { name: 'Indonesian', nativeName: 'Bahasa Indonesia', direction: 'ltr' },
  tr: { name: 'Turkish', nativeName: 'Türkçe', direction: 'ltr' },
  ru: { name: 'Russian', nativeName: 'Русский', direction: 'ltr' },
};

/**
 * Get word meanings for a specific surah and ayah
 * @param {number} surahId - Surah number (1-114)
 * @param {number} ayahNumber - Ayah number
 * @param {string} language - Language code (en, ur, fr, de, id, tr, ru)
 * @returns {Array} Array of word meanings
 */
export function getWordMeanings(surahId, ayahNumber, language = 'en') {
  const surah = WORD_MEANINGS[surahId];
  if (!surah) return null;

  const ayah = surah[ayahNumber];
  if (!ayah) return null;

  return ayah.map(word => ({
    arabic: word.ar,
    meaning: word[language] || word.en, // Fallback to English
    transliteration: word.transliteration || '',
  }));
}

/**
 * Check if a surah has local word meanings available
 */
export function hasLocalWordMeanings(surahId) {
  return WORD_MEANINGS.hasOwnProperty(surahId);
}

/**
 * Get list of surahs with available word meanings
 */
export function getSurahsWithWordMeanings() {
  return Object.keys(WORD_MEANINGS).map(Number);
}

export default WORD_MEANINGS;
